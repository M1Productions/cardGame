DROP TABLE personCard IF EXISTS;
DROP TABLE actionCard IF EXISTS;
DROP TABLE eventCard IF EXISTS;

CREATE TABLE personCard
(
	pCardID		INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	name		VARCHAR(32),
	LS		VARCHAR(1),
	cost		INTEGER,
	atk		INTEGER,
	dfp		INTEGER,
	role1		VARCHAR(16),
	role2		VARCHAR(16),
);
CREATE TABLE eventCard
(
	eCardID		INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	name		VARCHAR(32),
	cost		INTEGER
);
CREATE TABLE actionCard
(
	aCardID		INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	name		VARCHAR(32),
	cost		INTEGER
);
